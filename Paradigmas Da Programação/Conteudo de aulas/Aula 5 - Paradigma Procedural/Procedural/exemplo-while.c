// Bibliotecas utilizadas no código
#include <stdio.h>

// Função principal, agora com retorno inteiro
int main() {

	// Declaração da variável
	short indice = 11;

	// Exemplo para mostrar o pré-decremento! Na linha 12 o indice é decrementado e depois utilizado. Experimente trocar por indice--.
	while (indice > 0)
		printf("%hi\n", --indice);

	// retorno da função main
	return 0;
}